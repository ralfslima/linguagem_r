print(paste("5 é menor 4", 5 < 4))
print(paste("10 é maior ou igual 10", 10 >= 10))
print(paste("15 é menor ou igual 10", 10 <= 10))
print(paste("7 é igual 7", 7 == 7))
print(paste("8 é diferente de 7", 8 != 7))

